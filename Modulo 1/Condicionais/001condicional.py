# Concional Simples

# Para condições em Python ultilizamos a palavra if que significa 'se' em português.
# Com a condicional, voce consegue executar um conjunto de codigo APENAS se o resultado da expressão for "verdadeiro" (true)

idade = int(input("Qual a sua idade?"))
if idade >= 18:
    print("Maior de idade")
else:
    print("Menor de idade")